// Program to check number is odd or even
#include<stdio.h>


int main()
{
    printf("D5A_ShashwatTripathi_60\n");
    int num;
    printf("Enter a number:");
    scanf("%d", &num);
    if(num%2==0)
    {
        printf("%d is Even", num);
    }
    else
    {
        printf("%d is Odd", num);
    }
    return 0;

}